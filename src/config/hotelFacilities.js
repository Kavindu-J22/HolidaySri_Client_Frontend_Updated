// Get all facilities as a flat array
export const getAllFacilities = () => {
  return hotelFacilities.flatMap(category => category.facilities);
};

// Get facility label by key
export const getFacilityLabel = (key) => {
  const allFacilities = getAllFacilities();
  const facility = allFacilities.find(f => f.key === key);
  return facility ? facility.label : key;
};

